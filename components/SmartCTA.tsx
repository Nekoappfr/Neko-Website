
import React, { useState, useRef, useEffect } from 'react';
import BookingDropdown from './BookingDropdown';

interface SmartCTAProps {
  onTrigger: () => void;
  onSearchStateChange?: (isOpen: boolean) => void;
}

const SmartCTA: React.FC<SmartCTAProps> = ({ onTrigger, onSearchStateChange }) => {
  const [isOpen, setIsOpen] = useState(false);
  const [selectedDates, setSelectedDates] = useState('');

  const handleDatesSelected = (start: string, end: string) => {
    setSelectedDates(`${start} → ${end}`);
  };

  const toggleSearch = (open: boolean) => {
    setIsOpen(open);
    if (onSearchStateChange) {
      onSearchStateChange(open);
    }
  };

  return (
    <div className="w-full relative">
      {!isOpen ? (
        <div 
          className="relative flex items-center bg-white/95 backdrop-blur-md border border-white/20 rounded-xl p-1 pr-1 cursor-pointer group shadow-lg"
          onClick={() => toggleSearch(true)}
        >
          <div className="flex-1 px-3 py-2">
            <p className="text-[13px] md:text-[14px] font-bold text-[#37352F] group-hover:text-[#C25E72] transition-colors">
              Déposer une annonce gratuitement
            </p>
          </div>
          
          <button 
            className="w-8 h-8 bg-[#C25E72] rounded-lg flex items-center justify-center text-white shadow-sm hover:bg-[#A34D5E] transition-colors"
            onClick={(e) => {
              e.stopPropagation();
              toggleSearch(true);
            }}
          >
            <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="3" strokeLinecap="round" strokeLinejoin="round">
              <circle cx="11" cy="11" r="8"></circle>
              <line x1="21" y1="21" x2="16.65" y2="16.65"></line>
            </svg>
          </button>
        </div>
      ) : (
        <BookingDropdown 
          onClose={() => toggleSearch(false)} 
          onDatesSelected={handleDatesSelected}
        />
      )}
    </div>
  );
};

export default SmartCTA;
